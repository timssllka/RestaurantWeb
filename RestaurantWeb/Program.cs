using Microsoft.AspNetCore.Authentication.Cookies;
using Microsoft.AspNetCore.DataProtection;
using Microsoft.EntityFrameworkCore;
using RestaurantWeb;
using RestaurantWeb.Data;
using System;

var builder = WebApplication.CreateBuilder(args);

builder.Services.AddRazorPages();
builder.Services.AddResponseCaching();

builder.Services.AddDataProtection()
    .PersistKeysToFileSystem(new DirectoryInfo("/var/aspnet-keys/"))
    .SetApplicationName("RestaurantWeb");

// Настройка политик авторизации
builder.Services.AddAuthorization(options =>
{
    // Политика только для администраторов
    options.AddPolicy("AdminOnly", policy =>
        policy.RequireRole("администратор"));
    

});

// Добавляем аутентификацию через куки
builder.Services.AddAuthentication(CookieAuthenticationDefaults.AuthenticationScheme)
    .AddCookie(options =>
    {
        options.LoginPath = "/Login"; // Путь к странице входа
        options.AccessDeniedPath = "/AccessDenied";
        options.Cookie.HttpOnly = true;
        options.ExpireTimeSpan = TimeSpan.FromHours(1); // Время жизни куки

    });

builder.Services.AddDbContext<DiplomdbContext>(options =>
    options.UseNpgsql(builder.Configuration.GetConnectionString("DefaultConnection")));


// Настройка логгирования
builder.Logging.ClearProviders();
builder.Logging.AddConsole(); // Логи в консоль
builder.Logging.AddDebug();   // Логи в Debug-окно (Visual Studio)

var app = builder.Build();

// Middleware
app.UseHttpsRedirection();
app.UseStaticFiles();
app.UseRouting();
app.UseAuthentication();
app.UseAuthorization();

//добавляем поддержку маршрутизации razor page
app.MapRazorPages();
app.Run();
